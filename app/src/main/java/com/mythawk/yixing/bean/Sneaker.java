package com.mythawk.yixing.bean;

public class Sneaker {

    private int sneaker_id;
    private String sneaker_name;
    private String sneaker_band;
    private int sneaker_category;
    private int sneaker_price;
    private String sneaker_img;

    public int getSneaker_id() {
        return sneaker_id;
    }

    public void setSneaker_id(int sneaker_id) {
        this.sneaker_id = sneaker_id;
    }

    public String getSneaker_name() {
        return sneaker_name;
    }

    public void setSneaker_name(String sneaker_name) {
        this.sneaker_name = sneaker_name;
    }

    public String getSneaker_band() {
        return sneaker_band;
    }

    public void setSneaker_band(String sneaker_band) {
        this.sneaker_band = sneaker_band;
    }

    public int getSneaker_category() {
        return sneaker_category;
    }

    public void setSneaker_category(int sneaker_category) {
        this.sneaker_category = sneaker_category;
    }

    public int getSneaker_price() {
        return sneaker_price;
    }

    public void setSneaker_price(int sneaker_price) {
        this.sneaker_price = sneaker_price;
    }

    public String getSneaker_img() {
        return sneaker_img;
    }

    public void setSneaker_img(String sneaker_img) {
        this.sneaker_img = sneaker_img;
    }
}
